/**
  VALORES vs REFERENCIAS EN JS

    - Las variables en JS son independientes
    - Si una variable contiene un valor primitivo (el que sea) cuando se la asignemos a otra variable, se va a realizar
      una copia del valor hacia otra variable, pero no hay ninguna relación entre ellas
    
    - OBJETOS EN JS: cuando decimos "objeto" nos estamos refiriendo a un objeto literal, un array o una función, básicamente
      cualquier cosa que no sea un valor primitivo

    - HEAP (memoria dinámica): es el área de memoria destinada para almacenar objetos

    - REFERENCIA: la posición de memoria que se usa para acceder a aun objeto
 */
